        original_kws = data[stage_key].get('keywords', [])
        new_kws = []
        for kw in original_kws:
            if kw in kw_map:
                replacement = kw_map[kw]
                if isinstance(replacement, list):
                    new_kws.extend(replacement)
                else:
                    new_kws.append(replacement)
            else:
                new_kws.append(kw)
        
        # Deduplicate while preserving order just in case
        seen = set()
        dedup_kws = []
        for k in new_kws:
            if k not in seen:
                seen.add(k)
                dedup_kws.append(k)
                
        data[stage_key]['keywords'] = dedup_kws

